using UnityEngine;
using System.Collections;

public class WeaponAttr
{
    /// <summary>
    /// 武器火力
    /// </summary>
    public float Power;
    /// <summary>
    /// 武器的最大火力
    /// </summary>
    public float MaxPower;
    /// <summary>
    /// 稳定性
    /// </summary>
    public float Stab;
    /// <summary>
    /// 弹夹容量
    /// </summary>
    public float Clip;
    /// <summary>
    /// 瞄准镜倍数
    /// </summary>
    public float Ammo;

    /// <summary>
    /// 红外线时间
    /// </summary>
    public float Infra;
   
}
